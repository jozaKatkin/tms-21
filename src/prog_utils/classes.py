class Math:
    def __init__(self, num1, num2):
        self.num1 = num1
        self.num2 = num2

    def plus(self, num1, num2):
        return num1 + num2

    def minus(self, num1, num2):
        return num1 - num2

    def multi(self, num1, num2):
        return num1 * num2

    def divide(self, num1, num2):
        return num1 / num2

